package ex02;

public class Student {
	//Fields :속성
	int num;
	String name;
	int jumsu;
	
	//Getter Setter
	
	//Constructor 생성자
	//객체 new 할때 객체의 field 변수에 변수에 초기값을 지정
	public Student(int num, String name, int jumsu) {	
		this.num = num;
		this.name = name;
		this.jumsu = jumsu;
	}

	public Student() {}
	
	//기능(Method 함수)
	
	
	
}
